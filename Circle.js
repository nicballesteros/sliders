//Circle.js
class Circle {
    constructor(x, y, size) {
        this.size = size;
        this.x = x;
        this.y = y;
    }

    getSize() {
        return this.size;
    }
    getX() {
        return this.x;
    }
    setX(x) {
        this.x = x
    }
    getY() {
        return this.y;
    }
    setSize(x) {
        this.size = x;
    }


}
